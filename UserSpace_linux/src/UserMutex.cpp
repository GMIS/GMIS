#include "UserMutex.h"


//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CUserMutex::CUserMutex()
{

   int ret = pthread_mutex_init(&m_Mutex, NULL);
   assert(ret==0);
}

CUserMutex::~CUserMutex()
{
	  pthread_mutex_destroy(&m_Mutex);

}

